def find_team(cid, teams):
    for t in teams:
        for p in t.members:
            if p.chatid == cid:
                return t
    return None


def find_player(cid, players):
    for p in players:
        if p.chatid == cid:
            return p
    return None


def team_leader(team):
    for p in team.members:
        if p.leader:
            return p
    return None


async def send_msg_team(bot, team, msg):
    for p in team.members:
        await bot.send_message(p.chatid, msg)


